function woo_checkout_customizer_enqueue_scripts() {
    if (is_checkout()) {
        wp_enqueue_script('autocomplete-comuna-js', plugin_dir_url(__FILE__) . 'autocomplete-comuna.js', array('jquery'), null, true);
        wp_enqueue_style('autocomplete-style', plugin_dir_url(__FILE__) . 'autocomplete-style.css');
    }
}
add_action('wp_enqueue_scripts', 'woo_checkout_customizer_enqueue_scripts');

// Change the label of the City field to Comuna in WooCommerce checkout
function el_villegas_custom_checkout_field_labels($fields) {
    if (isset($fields['billing']['billing_city'])) {
        $fields['billing']['billing_city']['label'] = __('Comuna', 'woocommerce');
    }
    return $fields;
}
add_filter('woocommerce_billing_fields', 'el_villegas_custom_checkout_field_labels');


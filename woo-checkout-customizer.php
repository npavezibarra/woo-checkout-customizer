// Add custom validation for the Comuna field
function validate_comuna_field() {
    global $valid_comunas;

    // Get the entered "comuna" value
    $comuna = isset($_POST['billing_city']) ? sanitize_text_field($_POST['billing_city']) : '';

    // Validate comuna
    if (!in_array($comuna, $valid_comunas)) {
        wc_add_notice(__('Please enter a valid comuna.', 'woocommerce'), 'error');
    }
}
add_action('woocommerce_checkout_process', 'validate_comuna_field');


